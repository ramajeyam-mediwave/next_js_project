const generateUniqueRandomNumber = () => {
  const teamImg = Object.entries(funnyImgDetails);
  var arr = [];
  while (arr.length < 23) {
    var r = Math.floor(Math.random() * teamImg.length);
    if (arr.indexOf(teamImg[r][1]) === -1) arr.push(teamImg[r][1]);
  }
  return arr;
};

export const dynamicProfile = () => {
  const arrayList = generateUniqueRandomNumber();
  let count = 0;
  for (let i = 0; i < dyanmicBubbles.length; i++) {
    if (dyanmicBubbles[i].i) {
      dyanmicBubbles[i].i = arrayList[count] || Funny109URL;

      count += 1;
    }
  }
  return [...dyanmicBubbles];
};
